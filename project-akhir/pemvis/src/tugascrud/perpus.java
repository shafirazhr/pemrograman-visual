public void tampilData(){
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("NO_INDUK");
        tbl.addColumn("NAMA");
        tbl.addColumn("NAMA_BUKU");
        tbl.addColumn("TGL_PINJAM");
        tbl.addColumn("TGL_PENGEMBALIAN");
               
        tabeldata.setModel(tbl); // tbl_mahasiswa sesuaikan dengan variable pada table
        try {
            Statement st = conn.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM peminjaman");
            while (rs.next()) {
                tbl.addRow( new Object[] {
                    rs.getString("no_induk"),
                    rs.getString("nama"),
                    rs.getString("nama_buku"),
                    rs.getString("tgl_pinjam"),   
                    rs.getString("tgl_pengembalian")
                });
                tabeldata.setModel(tbl);
            }
        } catch (Exception e) {
        }
    }

