    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
    int selectedRow = tabeldata.getSelectedRow();

    if (selectedRow >= 0) {
        String kode = tabeldata.getValueAt(selectedRow, 2).toString();
        try {
            Statement st = conn.getConnection().createStatement();
            st.executeUpdate("DELETE FROM peminjaman WHERE no_induk = '" + kode + "'");
            st.close();
            DefaultTableModel model = (DefaultTableModel) tabeldata.getModel();
            model.removeRow(selectedRow);
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih baris yang akan dihapus terlebih dahulu.");
    }   // TODO add your handling code here:
    }//GEN-LAST:event_hapusActionPerformed

